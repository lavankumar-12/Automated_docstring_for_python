def add(a: int, b: int) -> int:
    """Add two numbers.

    :param a: The first number.
    :param b: The second number.
    :return: The sum.
    """
    return a + b


class Calculator:
    """A simple calculator class."""

    def multiply(self, x: float, y: float) -> float:
        """Multiply two numbers.

        :param x: First factor.
        :param y: Second factor.
        :return: Product.
        """
        return x * y
